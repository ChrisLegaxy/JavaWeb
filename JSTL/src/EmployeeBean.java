public class EmployeeBean {
    private String name;
    private int id;
    EmployeeBean(){
        super();
    }

    public String getName() {
        return name;
    }

    public int getId() {
        return id;
    }

    public void setName(String name) {
        this.name = name;
    }

    public void setId(int id) {
        this.id = id;
    }
}
